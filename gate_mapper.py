# ------------------------------------------------------------
# Función que obtiene las coordenadas (manual o automática)
# ------------------------------------------------------------
def get_gate_coordinates(gate):
    # 1. Si la puerta tiene coordenadas manuales, usarlas
    if gate in MANUAL_GATE_COORDS:
        return MANUAL_GATE_COORDS[gate]
    
    # 2. Si no, calcular automáticamente
    try:
        num = int(gate)
    except ValueError:
        num = 0
    
    if gate in TERMINAL1_GATES:
        # Terminal 1
        x = -99.08699573039003 + (num / 500)
        y = 19.4322872560707 + (num / 800)
    else:
        # Terminal 2
        x = -99.0818276942314 + (num / 500)
        y = 19.4211985763895 + (num / 800)
    return [y, x]

# ------------------------------------------------------------
# Crear el mapa interactivo con los estados de las puertas
# ------------------------------------------------------------
def create_gate_map(gates_status):
    """
    gates_status: dict { 'puerta': 'green', 'puerta2': 'red', ... }
    """
    m = folium.Map(location=AICM_CENTER, zoom_start=15, control_scale=True)
    
    # Diccionario para traducir estado a texto amigable
    estado_texto = {
        'green': 'Disponible',
        'red': 'Ocupado',
        'yellow': 'Próximo a liberar'
    }
    
    for gate, status in gates_status.items():
        coords = get_gate_coordinates(gate)
        
        # Asignar color e icono según estado
        if status == 'green':
            color = 'green'
            icon = 'check-circle'
            texto = estado_texto['green']
        elif status == 'yellow':
            color = 'orange'
            icon = 'exclamation-triangle'
            texto = estado_texto['yellow']
        else:  # red
            color = 'red'
            icon = 'times-circle'
            texto = estado_texto['red']
        
        folium.Marker(
            location=coords,
            popup=folium.Popup(f"Puerta {gate}<br>Estado: {texto}", max_width=200),
            icon=folium.Icon(color=color, icon=icon, prefix='fa')
        ).add_to(m)
    
    # Leyenda fija en el mapa 
    legend_html = '''
    <div style="position: fixed; bottom: 50px; left: 50px; z-index:1000; background-color:grey; padding:10px; border-radius:8px; border:2px solid gray; font-size:14px;">
    <b>Estado de puertas</b><br>
    <i class="fa fa-check-circle" style="color:green"></i> Disponible (verde)<br>
    <i class="fa fa-exclamation-triangle" style="color:orange"></i> Próximo a liberar (amarillo)<br>
    <i class="fa fa-times-circle" style="color:red"></i> Ocupado (rojo)
    </div>
    '''
    m.get_root().html.add_child(folium.Element(legend_html))
    return m